const int piece_colors[NUM_PIECES] = {1, 2, 3, 4, 5, 6, 7};

Piece piece_random(void) {
  Piece p;
  p.type = (PieceType)(rand() % NUM_PIECES);
  p.rot = 0;
  p.x = BOARD_W / 2 - 2;
  p.y = 0;
  return p;
}

bool piece_fits(const Game *g, const Piece *p) {
  for (int r = 0; r < PIECE_SIZE; r++) {
    int row_mask = shapes[p->type][p->rot][r];
    for (int c = 0; c < PIECE_SIZE; c++) {
      if (!(row_mask & (0x8 >> c)))
        continue;
      int bx = p->x + c;
      int by = p->y + r;
      if (bx < 0 || bx >= BOARD_W)
        return false;
      if (by >= BOARD_H)
        return false;
      if (by >= 0 && g->board[by][bx])
        return false;
    }
  }
  return true;
}

void piece_lock(Game *g) {
  Piece *p = &g->current;
  for (int r = 0; r < PIECE_SIZE; r++) {
    int row_mask = shapes[p->type][p->rot][r];
    for (int c = 0; c < PIECE_SIZE; c++) {
      if (!(row_mask & (0x8 >> c)))
        continue;
      int bx = p->x + c;
      int by = p->y + r;
      if (by >= 0 && by < BOARD_H && bx >= 0 && bx < BOARD_W)
        g->board[by][bx] = (int)p->type + 1;
    }
  }
}
